// Login to Metamask and check the if the user exists else creates one
async function claimToken() {
    let res = await connectToMetamask();
    if( res === true ) {
        let provider = new ethers.providers.Web3Provider( window.ethereum );
        let signer = provider.getSigner();
        try {
            const address = await signer.getAddress();         
            const contract = new ethers.Contract( FAUCET_CONTRACT_ADDRESS, faucetABI, signer );
            contract.request()
                .then(console.log)
                .catch((res) => alert(res.data.message));
        } catch(err) {
            console.log(err);
        }
    } else {
        alert("Couldn't connect to Metamask");
    }    
}

// Check if the Metamask connects 
async function connectToMetamask() {
    try {
        await window.ethereum.enable();
        return true;
    } catch(err) {
        return false;
    }
}
